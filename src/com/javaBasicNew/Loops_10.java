package com.javaBasicNew;

public class Loops_10 {
    public static void main(String[] args) {
        // loops

        // for loop
        for (int i = 0; i < 10; i++){
//            System.out.println(i);
        }

        // while
        int i = 0;
        while (i<10) {
//            System.out.println(i);
            i++;
        }

        // do while
        int k = 0;
        do {
            System.out.println(k);
            k++;
        } while (k<10);
    }
}
